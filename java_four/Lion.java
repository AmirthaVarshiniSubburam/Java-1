package java_four;

public class Lion extends Animal {

	@Override
	public void eat() {
		System.out.println("Lion loves to eat");
				
	}

	@Override
	public void hunt() {
		System.out.println("Lion hunts its prey");
				
	}

	@Override
	public void sleep() {
		System.out.println("Lion sleeps well");
		
	}

}
